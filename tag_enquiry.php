    <?php
        // Database connection parameters
        $servername = "localhost";
        $username = "root";
        $password = "";
        $dbname = "CRM";

        // Create connection
        $conn = new mysqli($servername, $username, $password, $dbname);

        // Check connection
        if ($conn->connect_error) {
            die("Connection failed: " . $conn->connect_error);
        }

        if ($_SERVER["REQUEST_METHOD"] == "POST") { 
        // Get form data
            $date_time = $_POST['date_n_time'];
            $query = $_POST['Query'];
            $call_type = $_POST['Call'];
            $resolution = $_POST['Resolution']; 
            $type = $_POST['type'];
            $sub_type = $_POST['subtype'];
            $resolution_status = $_POST['R_Status'];
            $customer_id = $_POST['ctr_id'];
            $remark = $_POST['remark'];

            // Prepare SQL statement
            $sql = "INSERT INTO tag_enquiry (customer_id, Tag_Date_Time, Call_Type, Type, Sub_Type, Query, Resolution, Resolution_Status, Remark) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            $stmt = $conn->prepare($sql);
            $stmt->bind_param("sssssssss", $customer_id, $date_time, $call_type, $type, $sub_type, $query, $resolution, $resolution_status, $remark);

            // Execute SQL statement
            if ($stmt->execute()) {
                echo "New record created successfully";
            } else {
                echo "Error: " . $sql . "<br>" . $conn->error;
            }
            $stmt->close();
        }

        // Close connection
        
        $conn->close();
    ?>

